# ---------------------------------------------------------------------------
# xlsx reader
# ---------------------------------------------------------------------------

def _read_sections(path: Path) -> dict[str, list[tuple[str, str]]]:
    """Read a generated company profile xlsx into section → [(label, value)] map."""
    wb = openpyxl.load_workbook(str(path), read_only=True)
    ws = wb.active

    sections: dict[str, list[tuple[str, str]]] = {}
    current: str | None = None

    for row in ws.iter_rows(values_only=True):
        col_a = row[0]
        col_b = row[1] if len(row) > 1 else None

        if col_a is None:
            continue

        label = str(col_a).strip()
        upper = label.upper()

        # Section header: col A matches a known section name, col B is empty
        if upper in SECTION_NAMES and not col_b:
            current = upper
            sections[current] = []
            continue

        # Skip the title row
        if upper == "COMPANY PROFILE":
            continue

        if current is not None and label:
            value = str(col_b).strip() if col_b is not None else ""
            sections[current].append((label, value))

    wb.close()
    return sections


def _kv(rows: list[tuple[str, str]]) -> dict[str, str]:
    """Convert a list of (label, value) pairs to a flat label → value dict."""
    return {label: value for label, value in rows}


# ---------------------------------------------------------------------------
# Type helpers
# ---------------------------------------------------------------------------

def _safe_float(val: str) -> float | None:
    """Parse a string to float, returning None on empty or invalid input."""
    if not val:
        return None
    try:
        return float(val)
    except ValueError:
        return None


def _safe_int(val: str) -> int | None:
    """Extract the first integer from a string (e.g. '~250' → 250), or None."""
    if not val:
        return None
    m = re.search(r"\d+", val)
    return int(m.group()) if m else None


def _safe_bool(val: str) -> bool | None:
    """Parse a Yes/No cell value to bool.  Returns None for empty strings."""
    if not val:
        return None
    return val.strip().lower().startswith("y")


def _glassdoor_rating(val: str) -> float | None:
    """Extract a decimal rating like '4.2 / 5.0' from a Glassdoor cell, or None."""
    if not val:
        return None
    m = re.search(r"\d+\.\d+", val)
    return float(m.group()) if m else None


