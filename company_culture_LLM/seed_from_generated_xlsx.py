_SECTION_PARSERS = {
    "COMPENSATION":                  ("compensation",       _parse_compensation),
    "INTERVIEW PROCESS":             ("interview",          _parse_interview),
    "REMOTE / HYBRID POLICY":        ("remote_policy",      _parse_remote),
    "GLASSDOOR / EMPLOYEE RATINGS":  ("glassdoor",          _parse_glassdoor),
    "RECENT NEWS & CONTROVERSIES":   ("news",               _parse_news),
    "RECRUITING & CONTACTS":         ("recruiting",         _parse_recruiting),
    "TECH STACK":                    ("tech_stack",         _parse_tech_stack),
    "INTERNSHIP PROGRAM":            ("internship",         _parse_internship),
    "WORKFORCE SIGNALS":             ("workforce_signals",  _parse_workforce),
}


def parse_generated_xlsx(path: Path) -> CompanyProfile:
    """Parse a single generated company profile xlsx into a CompanyProfile."""
    sections = _read_sections(path)

    overview = _parse_overview(sections.get("COMPANY OVERVIEW", []))
    executives = _parse_executives(sections.get("EXECUTIVE LEADERSHIP", []))
    culture = _parse_culture(sections.get("CULTURE & VALUES", []))

    kwargs: dict = {"overview": overview, "executives": executives, "culture": culture}
    for section_key, (field, parser_fn) in _SECTION_PARSERS.items():
        rows = sections.get(section_key, [])
        if rows:
            kwargs[field] = parser_fn(rows)

    return CompanyProfile(**kwargs)


