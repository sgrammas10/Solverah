def create_app() -> Flask:
    app = Flask(__name__)
    app.config["SQLALCHEMY_DATABASE_URI"] = os.environ["DATABASE_URL"]
    app.config["SQLALCHEMY_TRACK_MODIFICATIONS"] = False
    db.init_app(app)
    return app


def run_seed(verbose: bool = True) -> None:
    """Discover all .xlsx files in company_profiles/ and upsert them into the DB.

    Reports per-file insert/update/error status to stdout.  Rolls back and
    continues on per-file errors so one bad file doesn't abort the entire run.

    Args:
        verbose: Print per-file status lines (default True).  Set to False for
                 quieter CI/cron execution.
    """
    xlsx_files = sorted(COMPANY_PROFILES.glob("*.xlsx"))
    if not xlsx_files:
        print(f"No .xlsx files found in {COMPANY_PROFILES}")
        sys.exit(0)

    print(f"Found {len(xlsx_files)} file(s) in {COMPANY_PROFILES}\n")

    app = create_app()
    with app.app_context():
        seeded, updated, errors = 0, 0, 0
        for path in xlsx_files:
            try:
                profile = parse_generated_xlsx(path)
                name = profile.overview.name

                slug = re.sub(r"[^a-z0-9]+", "-", name.lower()).strip("-")
                existing = Company.query.filter_by(slug=slug).first()

                upsert_company(profile)

                if existing:
                    updated += 1
                    if verbose:
                        print(f"  updated  {name}  ({path.name})")
                else:
                    seeded += 1
                    if verbose:
                        print(f"  inserted {name}  ({path.name})")
            except Exception as exc:
                errors += 1
                db.session.rollback()
                print(f"  ERROR {path.name}: {exc}")

        print(f"\nDone — {seeded} inserted, {updated} updated, {errors} errors.")


if __name__ == "__main__":
    run_seed()
