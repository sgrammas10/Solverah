def _parse_overview(rows: list[tuple[str, str]]) -> CompanyOverview:
    kv = _kv(rows)
    return CompanyOverview(
        name=kv.get("Name") or "Unknown",
        founded=_safe_int(kv.get("Founded", "")),
        website=kv.get("Website") or None,
        industry=kv.get("Industry") or None,
        size=kv.get("Size") or None,
        headquarters=kv.get("Headquarters") or None,
        ownership=kv.get("Ownership") or None,
        revenue_est=kv.get("Revenue (est.)") or None,
        headcount_est=kv.get("Headcount (est.)") or None,
    )


def _parse_executives(rows: list[tuple[str, str]]) -> list[Executive]:
    """Executives are stored as 'Executive N — Name / Title / LinkedIn URL' rows."""
    by_index: dict[int, dict[str, str]] = {}
    for label, value in rows:
        m = re.match(r"Executive (\d+) — (.+)", label)
        if not m:
            continue
        idx = int(m.group(1))
        field = m.group(2)
        by_index.setdefault(idx, {})[field] = value

    execs = []
    for idx in sorted(by_index):
        d = by_index[idx]
        name = d.get("Name", "").strip()
        if not name:
            continue
        execs.append(Executive(
            name=name,
            title=d.get("Title", "").strip(),
            linkedin_url=d.get("LinkedIn URL") or None,
        ))
    return execs


def _parse_culture(rows: list[tuple[str, str]]) -> CultureValues | None:
    kv = _kv(rows)
    # Required numeric fields — skip section entirely if all missing
    empathy = _safe_float(kv.get("Empathy (0.0\u20131.0)", ""))
    if empathy is None:
        return None
    return CultureValues(
        culture_narrative_company=kv.get("Culture Narrative (Company Says)") or None,
        culture_narrative_employees=kv.get("Culture Narrative (Employees Say)") or None,
        core_values=[v.strip() for v in kv.get("Core Values", "").split(",") if v.strip()],
        work_environment=kv.get("Work Environment") or None,
        pace=kv.get("Pace") or "moderate",
        empathy=empathy,
        creative_drive=_safe_float(kv.get("Creative Drive (0.0\u20131.0)", "")) or 0.0,
        adaptability=_safe_float(kv.get("Adaptability (0.0\u20131.0)", "")) or 0.0,
        futuristic=_safe_float(kv.get("Futuristic (0.0\u20131.0)", "")) or 0.0,
        harmony=_safe_float(kv.get("Harmony (0.0\u20131.0)", "")) or 0.0,
        data_orientation=_safe_float(kv.get("Data Orientation (0.0\u20131.0)", "")) or 0.0,
    )


